const Faqpage = () => {
  const [selevctcategory, setSelectcategory] = useState("food");
  const [selectedButton, setSelectedButton] = useState(1);
  const [questionslist , setquestionslist]=useState(foodlist);
  const getButtonStyle = (buttonId) => {
    return selectedButton === buttonId ? { backgroundColor: '#35b8be', color: 'white' } : {};
  };

  const handlechange = (buttonId,category) => {
    setSelectedButton(buttonId);
    if(category == "food")
    {
      setquestionslist(foodlist);
    }
    if(category == "delivery")
    {
      setquestionslist(deliveryquestions);
    }
    if(category == "company")
    {
      setquestionslist(foodlist);
    }
    setSelectcategory(companyquestions);
  };

  return (
    <div className="faqmainpage">
      <div className="faq1">
        <p className="head faqhead">
          Frequently Asked <span>Questions</span>
        </p>
      </div>
      <div className="fqabuttons">
        <button className="btn" style={getButtonStyle(1)} onClick={() => handlechange(1,"food")}>
          Our Food
        </button>
        <button className="btn" style={getButtonStyle(2)} onClick={() => handlechange(2,"delivery")}>
          {" "}
          Our Delivery
        </button>
        <button className="btn" style={getButtonStyle(3)} onClick={() => handlechange(3,"company")}>
          Our Company
        </button>
      </div>
      {
        <div className="questionslist">
          <div className="questions">
            
            {questionslist.map((q) => (
              <Question
                key={q.id}
                question={q.question}
                solution={q.solution}
              />
            ))}
          </div>
        </div>
      }
      <Fotter/>
    </div>
  );
};

